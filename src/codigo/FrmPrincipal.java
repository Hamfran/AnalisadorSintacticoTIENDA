    private void analizarLexico() throws IOException {
      DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); // Limpiar tabla antes de cada análisis
    int cont = 1;
    
    String expr = txtResultado.getText();
    Lexer lexer = new Lexer(new StringReader(expr));
    String resultado = "LINEA " + cont + "\t\tSIMBOLO\n";
    Object[] fila = new Object[2]; // Ajuste para filas en la tabla

    while (true) {
        Tokens token = lexer.yylex();
        if (token == null) {
            jTable1.setModel(modelo);
            return;
        }

        switch (token) {
            case Linea:
                cont++;
                resultado += "LINEA " + cont + "\n";
                break;
            case NombreComprador:
            case CodigoPostal:
            case Ubicacion:
            case Cantidad:
            case Precio:
            case DescripcionArticulo:
            case Guion:
                resultado += "  <" + token + ">\t\t" + lexer.lexeme + "\n";
                fila[0] = token.name();
                fila[1] = lexer.lexeme;
                modelo.addRow(fila);
                break;
            case ERROR:
                resultado += "  <Simbolo no definido>\n";
                fila[0] = "No definido";
                fila[1] = lexer.lexeme;
                modelo.addRow(fila);
                break;
            default:
                resultado += "  < " + lexer.lexeme + " >\n";
                fila[0] = "Error";
                fila[1] = lexer.lexeme;
                modelo.addRow(fila);
                break;
        }
    }
    }

